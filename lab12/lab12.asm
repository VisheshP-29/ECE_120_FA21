; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

; This program uses the data from cases folder and the different letter.asm files in the folder
; to print the ASCII character in a large font. In order to implement the file, I first loaded
; the ASCII character into a register and multiplied it by 16 so that it can be used as a
; pointer. I then used a nested for-loop to iterate through the rows and then the columns
; in order to print the values. To determine the values to be printed at each space, I checked
; the most significant bit in R6, and checked the next bit by left shifting R6.

; This program primarily uses registers in the following manner;
; R0 contains the ASCII symbol to be printed by the program
; R1 contains the starting address of the FRONT_FILE register at x5000
; R2 contains the ASCII value of the character rendered in a large font
; R3 contains the temp values
; R4 contains the counter for the rows
; R5 contains the counter for the columns
; R6 contains the content of the row address


	.ORIG x3000		;start program at location x3000

	AND	R0,R0,#0	;initialize R0	
	AND	R1,R1,#0	;initialize R1
	AND	R2,R2,#0	;initialize R2
	AND	R3,R3,#0	;initialize R3
	AND	R5,R5,#0	;initialize R5
	AND	R6,R6,#0	;initialize R6

	LD	R1,FONT_FILE	;load starting address of x5000 into R1
	LDR	R2,R1,#2	;load ascii value of charater stored at address x5002
	ADD	R4,R4,#8	;set counter to multiply ascii value by 16
	ADD	R4,R4,#8	;set counter to multiply ascii value by 16
MULT	ADD	R3,R2,R3	;loop for multiplication
	ADD	R4,R4,#-1	;decrement counter
	BRp	MULT		;loop back until R4 is zero
	AND	R2,R2,#0	;clear R2
	ADD	R2,R2,R3	;store R3 in R2
	AND	R3,R3,#0	;clear R3
	AND	R4,R4,#0	;clear R4
	LEA	R3,FONT_DATA	;load R3 with address of FONT_DATA
	ADD	R2,R2,R3	;load R2 with starting row address of the ascii character
	ADD	R4,R4,#8	;set R4 as row counter to count 16 rows
	ADD	R4,R4,#8	;set R4 as row counter to count 16 rows
COL	AND	R5,R5,#0	;clear R5
	ADD	R5,R5,#8	;set R5 as column counter to count 8 columns of each row
	LDR	R6,R2,#0	;load content of row address into R6
CHECK	BRn	NEG		;check if most significant bit is 0 or 1
	LDR	R0,R1,#0	;print symbol for "0"
	OUT			;print
	BRnzp	DEC		;branch to column decrementer
NEG	LDR	R0,R1,#1	;print symbol for "1"
	OUT			;print
DEC	ADD	R5,R5,#-1	;decrement column counter
	BRz	LINE		;branch to make new line
	ADD	R6,R6,R6	;left shift R6
	BRnzp	CHECK		;branch back to check if R6 is negative or positive
LINE	AND	R0,R0,#0	;branch for new line and clear R0
	LD	R0,NEW_LINE	;print new line
	OUT			;print
	ADD	R2,R2,#1	;increment row location
	ADD	R4,R4,#-1	;decrement row counter
	BRp	COL		;loop for next row
	HALT			;Exit
FONT_FILE	.FILL	x5000
NEW_LINE	.FILL	x0A
