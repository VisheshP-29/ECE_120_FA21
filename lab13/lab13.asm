; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

; This program uses the data from the cases folder and the corresponding 
; letter.asm files in the folder to print ASCII characters and the strings
; in large font. In order to implement this program I loaded in the starting
; ASCII character and printed the first row in the large font. I loaded in the 
; next ASCII character and printed its corresponding first row. I then went 
; through the rest of the string and printed the ASCII character and their 
; corresponing first row. After the first row was printed for each character,
; I went through the loop again and printed the second row for each character,
; repeating this step, row by row, to print the whole string.

; The program primarily uses registers in the following manner:
; R0 contains the ASCII symbol to be printed by the program and is used as a temp holder
; R1 contains the address of the ASCII character to be printed
; R2 contains the value of each ASCII character thats to be printed in larhe font
; R3 contains the address of FONT_DATA and increments to track each row
; R4 contains the counter for each row
; R5 contains the counter for each column and is used as a temp holder
; R6 contains the content of the row address

	.ORIG	x3000		;start program at location x3000

	AND	R0,R0,#0	;initialize R0
	AND	R1,R1,#0	;initialize R1
	AND	R2,R2,#0	;initialize R2
	AND	R3,R3,#0	;initialize R3
	LEA 	R3,FONT_DATA	;load R3 with address of FONT_DATA module 
	AND	R4,R4,#0	;initialize R4
	AND	R5,R5,#0	;initialize R5
	AND	R6,R6,#0	;initialize R6

	ADD	R4,R4,#8	;set row counter to count 16 rows
	ADD	R4,R4,#8	;set row counter to count 16 rows
FIRST	AND	R1,R1,#0	;clear R1
	LD	R1,CHAR_FILE	;load starting address of x5002 into R1
	AND	R2,R2,#0	;clear R2
	LDR	R2,R1,#0	;load ascii value of character
	BRz	LINE		;branch to next line if find null character
	BRnzp	REST		;branch to skip loop for character incrementation
INPUT	ADD	R1,R1,#1	;increment character
	AND	R2,R2,#0	;clear R2
	LDR	R2,R1,#0	;load ascii value of character
	BRz	LINE		;branch to next line
REST	AND	R5,R5,#0	;clear R5
	ADD	R5,R5,#8	;set counter to multiply ascii value by 16
	ADD	R5,R5,#8	;set counter to multiple ascii value by 16
	AND	R0,R0,#0	;clear R0
MULT	ADD	R0,R2,R0	;loop for multiplication
	ADD	R5,R5,#-1	;decrement counter
	BRp	MULT		;loop back until R5 is zero
	AND	R2,R2,#0	;clear R2
	ADD	R2,R2,R0	;store R0 in R2
	AND	R0,R0,#0	;clear R0	
	ADD	R2,R2,R3	;load R2 with starting row address of ascii character
	AND	R5,R5,#0	;clear R5
	ADD	R5,R5,#8	;set R5 as column counter to count 8 columns
	LDR	R6,R2,#0	;load content of row address into R6
CHECK	BRn	NEG		;check if most significant bit is 0 or 1
	AND	R0,R0,#0	;clear R0	
	LD	R0,ZERO		;load address for symbol "0"
	LDR	R0,R0,#0	;load value at address for "0"
	OUT			;print
	BRnzp	DEC		;branch to column decrementer
NEG	AND	R0,R0,#0	;clear R0
	LD	R0,ONE		;load address for symbol "1"
	LDR	R0,R0,#0	;load value at address for "1"
	OUT			;print
DEC	ADD	R5,R5,#-1	;decrement column counter
	BRz	INPUT		;branch to make new character
	ADD	R6,R6,R6	;left shift R6
	BRnzp	CHECK		;branch back to check if R6 is negative or positive
LINE	AND	R0,R0,#0	;clear R0
	LD	R0,NEW_LINE	;print new line
	OUT			;print
	ADD 	R3,R3,#1	;increment FONT_DATA pointer
	ADD	R4,R4,#-1	;decrement row counter
	BRp	FIRST		;loop for next row
	HALT			;exit

